public class variables{
    public static void main(String[] args) {
        int numero = 10;
        double preco = 19.99;
        boolean ativo = true;
        char letra = 'A';

        System.out.println("Número: " + numero);
        System.out.println("Preço: " + preco);
        System.out.println("Ativo: " + ativo);
        System.out.println("Letra: " + letra);
    }
	/*PRINCIPAIS TIPOS DE VARIAVEI EM JAVA:
	int numero = 10;
	double preco = 19.99;
	boolean ativo = true;
	char letra = 'A'; 
    Tipo	Caixa


    int	caixa de números inteiros
    double	caixa de números decimais
    String	caixa de texto
    boolean	caixa de verdadeiro/falso

*/
}
